          <el-dropdown-item divided @click.native="logout">
            <span>退出登录</span>
          </el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
    </div>
  </div>
</template>

<script>
import { mapGetters } from 'vuex'
import Breadcrumb from '@/components/Breadcrumb'
import TopNav from '@/components/TopNav'
import Hamburger from '@/components/Hamburger'
import Screenfull from '@/components/Screenfull'
import SizeSelect from '@/components/SizeSelect'
import Search from '@/components/HeaderSearch'
import RuoYiGit from '@/components/RuoYi/Git'
import RuoYiDoc from '@/components/RuoYi/Doc'
import AlarmBtn from '@/components/HeaderAlarmBtn'
import ThemeSwitch from "@/components/ThemeSwitch"
import AppCode from "@/components/AppCode"
import PlatOrder from "@/components/PlatOrder"
import { tenantList } from "@/api/login";
import { dynamicClear, dynamicTenant } from "@/api/system/tenant";
export default {
  data() {
    return {
      userId: this.$store.getters.userId,
      tenantId: undefined,
      companyName: undefined,
      tenantList: [],
      tenantEnabled: false,
      userList: []
    }
  },
  components: {
    Breadcrumb,
    TopNav,
    Hamburger,
    Screenfull,
    SizeSelect,
    Search,
    RuoYiGit,
    RuoYiDoc,
    AlarmBtn,
    ThemeSwitch,
    AppCode,
    PlatOrder
  },
  computed: {
    ...mapGetters([
      'sidebar',
      'avatar',
      'device'
    ]),
    setting: {
      get() {
        return this.$store.state.settings.showSettings
      },
      set(val) {
        this.$store.dispatch('settings/changeSetting', {
          key: 'showSettings',
          value: val
        })
      }
    },
    topNav: {
      get() {
        return this.$store.state.settings.topNav
      }
    },
    appletShow() {
      return this.$store.getters.logoInfo.appletShow;
    },
  },
  methods: {
    // 动态切换
    dynamicTenantEvent(tenantId) {
      if (this.companyName != null && this.companyName !== '') {
        dynamicTenant(tenantId).then(res => {
          this.$tab.closeAllPage()
          this.$router.push('/')
        });
      }
    },
    dynamicClearEvent() {
      dynamicClear().then(res => {
        this.$tab.closeAllPage()
        this.$router.push('/')
      });
    },
    // 租户列表
    getTenantList() {
      tenantList().then(res => {
        this.tenantList = res.data.voList;
        this.tenantEnabled = res.data.tenantEnabled;
      });
    },
    toggleSideBar() {
      this.$store.dispatch('app/toggleSideBar')
    },
    async logout() {
      this.$confirm('确定注销并退出系统吗？', '提示', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning'
      }).then(() => {
        this.$store.dispatch('LogOut').then(() => {
          //location.href = process.env.VUE_APP_CONTEXT_PATH + "index";
          location.href = this.$store.state.settings.indexPage;
        })
      }).catch(() => { });
    },
  }
}
</script>

