          <el-select v-model="companyName" clearable filterable reserve-keyword placeholder="请选择租户"
            v-if="userId === 1 && tenantEnabled" @change="dynamicTenantEvent" @clear="dynamicClearEvent">
            <el-option v-for="item in tenantList" :key="item.tenantId" :label="item.companyName" :value="item.tenantId">
            </el-option>
            <svg-icon slot="prefix" icon-class="company" class="el-input__icon input-icon" />
          </el-select>
